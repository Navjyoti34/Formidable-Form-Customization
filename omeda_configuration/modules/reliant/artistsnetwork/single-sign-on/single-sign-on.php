	function single_sign_on_cookie_dump($name, $value) {
	    $expirationTime = time() + 3600;

	    setcookie($name, $value, $expirationTime, "/");
	}

	function single_sign_on_user_data_reset($user_data) {
		$user_id = $user_data->ID;

		delete_user_meta($user_id, 'midtc_single_sign_on_uuid');
		delete_user_meta($user_id, 'midtc_single_sign_on_code');
		delete_user_meta($user_id, 'midtc_single_sign_on_data');
	}

	function single_sign_on_pattern_check($wp) {
	    $request_path = parse_url($_SERVER['REQUEST_URI'], PHP_URL_PATH);

	    if (!(stripos($request_path, 'magic') !== false)) {
	        return;
	    }

	    $pathSegments = explode('/', $request_path);

	    if (count(explode('/', trim($request_path, '/'))) > 0 && in_array($pathSegments[1], ['magic']) && isset($pathSegments[2])) {
	    	$user_single_sign_on_uuid = isset($pathSegments[2]) ? sanitize_text_field($pathSegments[2]) : null;

	    	$response = array('error' => true, 'msg' => 'A problem has arisen while attempting to log you in using a magic link.');

	    	if(!$user_single_sign_on_uuid) {
	    		single_sign_on_cookie_dump('magic_error_' . uniqid(), json_encode($response));

				die(wp_redirect(home_url('/my-account/')));
	    	}

	    	global $wpdb;

			$sql = $wpdb->prepare("
			    SELECT DISTINCT u.ID, u.user_login
			    FROM {$wpdb->users} u
			    JOIN {$wpdb->usermeta} um ON u.ID = um.user_id
			    WHERE um.meta_key = %s
			      AND um.meta_value = %s
			    LIMIT 1
			", 'midtc_single_sign_on_uuid', $user_single_sign_on_uuid);

			$result = $wpdb->get_row($sql);

			if ($result) {
			    $user_id = $result->ID;
				$user_data = get_userdata($user_id);


				if (!$user_data) {
					single_sign_on_cookie_dump('login_error_' . uniqid(), $response);
					
					die(wp_redirect(home_url('/my-account/')));
				}

			    $user_login = $result->user_login;

			    $user_single_sign_on_data = get_user_meta($user_id, 'midtc_single_sign_on_data', true);
			    $user_single_sign_on_code_data = get_user_meta($user_id, 'midtc_single_sign_on_code', true);

			    if(!$user_single_sign_on_data || !$user_single_sign_on_code_data) {
			    	$response['msg'] = 'An issue has occurred while attempting to log you in using a magic link.';

			    	single_sign_on_cookie_dump('magic_error_' . uniqid(), json_encode($response));

			    	single_sign_on_user_data_reset($user_data);

					die(wp_redirect(home_url('/my-account/')));
			    }

			    $user_single_sign_on_data_email = $user_single_sign_on_data['email'];
			    $user_single_sign_on_data_epoch = $user_single_sign_on_data['epoch'];
			    $user_single_sign_on_data_sso = $user_single_sign_on_data['sso'];
			    $user_single_sign_on_data_uuid = $user_single_sign_on_data['uuid'];

			    $user_single_sign_on_code_data_sso = $user_single_sign_on_code_data['code'];
			    $user_single_sign_on_code_data_epoch = $user_single_sign_on_code_data['epoch'];

			    if(!($user_single_sign_on_data_uuid == $user_single_sign_on_uuid)) {
			    	$response['msg'] = 'An unknown issue has occurred while attempting to log you in using a magic link.';

			    	single_sign_on_cookie_dump('magic_error_' . uniqid(), json_encode($response));

			    	single_sign_on_user_data_reset($user_data);

					die(wp_redirect(home_url('/my-account/')));
			    }

			    if(!($user_single_sign_on_code_data_sso == $user_single_sign_on_code_data_sso)) {
			    	$response['msg'] = 'An unexpected problem has arisen while trying to log you in using a magic link.';

			    	single_sign_on_cookie_dump('magic_error_' . uniqid(), json_encode($response));

			    	single_sign_on_user_data_reset($user_data);

					die(wp_redirect(home_url('/my-account/')));
			    }

			    $user_single_sign_on_data_epoch_check = time() - $user_single_sign_on_data_epoch;

			    if (($user_single_sign_on_data_epoch_check > 300)) {
					$response['msg'] = "That magic link has expired. Kindly request a new one for verification.";

			    	single_sign_on_cookie_dump('magic_error_' . uniqid(), json_encode($response));

			    	single_sign_on_user_data_reset($user_data);

					die(wp_redirect(home_url('/my-account/')));
				}

				$user_single_sign_on_code_data_epoch_check = time() - $user_single_sign_on_code_data_epoch;

			    if (($user_single_sign_on_code_data_epoch_check > 300)) {
					$response['msg'] = "That magic link has expired. Kindly request a new one to log into your account.";

			    	single_sign_on_cookie_dump('magic_error_' . uniqid(), json_encode($response));

			    	single_sign_on_user_data_reset($user_data);

					die(wp_redirect(home_url('/my-account/')));
				}

				single_sign_on_auto_login($user_id);

		        die(wp_redirect(home_url('/my-account/')));
			} else {
				$response['msg'] = "That magic link invalid. Kindly request a new one to log into your account.";

		    	single_sign_on_cookie_dump('magic_error_' . uniqid(), json_encode($response));

		    	single_sign_on_user_data_reset($user_data);

				die(wp_redirect(home_url('/my-account/')));
			}
	    	
	    	die(wp_redirect(home_url('/')));
	    } else {
	        return;
	    }
	}

