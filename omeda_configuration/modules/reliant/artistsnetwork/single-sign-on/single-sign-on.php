	function single_sign_on_code_processing_rest_api() {
		$request_type = isset($_GET['request']) ? sanitize_text_field($_GET['request']) : null;

		if(!$request_type) {
			return;
		}

		if($request_type != 'single_sign_on' && $request_type != 'single_sign_on_check') {
			return;
		}

		if($request_type == 'single_sign_on_check') {
			$output['logged_in'] = is_user_logged_in();

			die(json_encode($output));
		}

		$output = ['error' => true, 'msg' => 'Parameter issue'];

		if(is_user_logged_in()) {
			$output['msg'] = "You are already logged in at the moment.";

			die(json_encode($output));
		}

		$email_address = isset($_GET['email']) ? (sanitize_email($_GET['email']) ?: sanitize_text_field($_GET['email'])) : null;

		if(!$email_address) {
			$output['msg'] = 'You haven\'t provided a username or email address.';

			die(json_encode($output));
		}

		$user = get_user_by('email', $email_address) ?: get_user_by('login', $email_address);

	    if ($user) {
	        $user_id = $user->ID;
	        $user_data = get_userdata($user_id);

			if($request_type != 'single_sign_on') {
				die(json_encode($output));
			}

			$meta_key = 'midtc_single_sign_on_data';

			$pull_user_sign_on_data = get_user_meta($user_id, $meta_key, true);

			if ($pull_user_sign_on_data) {
			    if (!((time() - $pull_user_sign_on_data['epoch']) > 60)) {
					$output['msg'] = "We've already sent a magic link. Please wait for 60 seconds before trying again.";

					die(json_encode($output));
			    }
			}

			$user_email = $user_data->user_email;
	        $assigned_sso = single_sign_on_code_generation($user_data);
	        $assigned_uuid = single_sign_on_uuid($user_data);

			if (!$assigned_sso || !$assigned_uuid) {
			    $output['msg'] = "An error occurred while generating a magic link. Please attempt this again at a later time.";
			}

        	$data = ['email' => $user_email, 'epoch' => time(), 'sso' => $assigned_sso, 'uuid' => $assigned_uuid];

        	$assigned_data = single_sign_on_apply_data($user_data, $data);

        	if (!$assigned_data) {
			    $output['msg'] = "An error occurred while generating a magic link. Please attempt this again at a later time.";
			}

			$single_sign_on_login_code = $assigned_uuid;

			$redirect = isset($_GET['redirect']) ? esc_url($_GET['redirect']) : null;

            $send_sso_email = single_sign_on_send_out_email($redirect, $user_email, null, null, 'GPM240318014', 'Your Artists Network Magic Link', null, $single_sign_on_login_code, true);

            if ($send_sso_email) {
                $output['error'] = false;
                $output['msg'] = "Please check your email for the magic link to log into your account. It will expire in <span id='countdown'>60</span> seconds.";
            } else {
                $output['msg'] = "Error sending the magic link. Please try again later.";
            }
	        
	    } else {
	        $output['msg'] = 'Please double-check that you\'ve entered the right username or email. We couldn\'t locate an account associated with <b>' . $email_address . '</b>.';
	    }

		die(json_encode($output));
	}

	function single_sign_on_apply_data($user_data, $data) {
		$user_id = $user_data->ID;

		$assign_data = update_user_meta($user_id, 'midtc_single_sign_on_data',  $data);

		if(!$assign_data) {
			return false;
		}

		return true;
	}

