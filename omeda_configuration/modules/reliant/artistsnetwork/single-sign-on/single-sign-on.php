		if(!isset($send_decode_omeda_payload)) {
			return false;
		}

	    return $send_payload_to_omeda;
	}

	function single_sign_on_login_encrypt($data, $passphrase) {
	    $secret_key = hex2bin($passphrase);
	    $iv = openssl_random_pseudo_bytes(12); // Generate a random IV (Initialization Vector)
	    $encrypted_data = openssl_encrypt(json_encode($data), 'aes-256-gcm', $secret_key, OPENSSL_RAW_DATA, $iv, $tag);
	    
	    // Base64 encode the encrypted data, IV, and tag for storage
	    $encoded_data = base64_encode(json_encode([
	        'data' => base64_encode($encrypted_data),
	        'iv'   => base64_encode($iv),
	        'tag'  => base64_encode($tag),
	    ]));
	    
	    return $encoded_data;
	}

	function single_sign_on_login_decrypt($data, $passphrase) {
	    $secret_key = hex2bin($passphrase);
	    $json = json_decode(base64_decode($data), true);
	    $iv = base64_decode($json['iv']);
	    $tag = base64_decode($json['tag']);
	    $encrypted_data = base64_decode($json['data']);
	    $decrypted_data = openssl_decrypt($encrypted_data, 'aes-256-gcm', $secret_key, OPENSSL_RAW_DATA, $iv, $tag);
	    
	    return json_decode($decrypted_data, true);
	}

	function single_sign_on_code_generation($user_data) {
		$user_id = $user_data->ID;
		$code = sprintf('%06d', rand(0, 999999));

		$assign_sso = update_user_meta($user_id, 'midtc_single_sign_on_code',  array('code' => $code, 'epoch' => time()));

		if(!$assign_sso) {
			return false;
		}

		return $code;
	}

